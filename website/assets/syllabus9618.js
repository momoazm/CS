/* Starter flashcards — 9618 key terms (seeded once into the flashcards tool). */
window.STARTER_DECK_9618 = [
  ['Two’s complement','Representation of signed integers where the most significant bit carries a negative weight. To negate: invert all bits and add 1.'],
  ['Interrupt','A signal to the processor that an event needs attention. Serviced by the ISR after the current fetch–execute cycle completes; registers are saved first.'],
  ['Cache memory','Small, fast memory between the CPU and RAM that holds frequently used instructions/data to reduce access time.'],
  ['RISC vs CISC','RISC: few simple fixed-length instructions, many registers, suits pipelining. CISC: many complex variable-length instructions, fewer registers.'],
  ['Pipelining','Overlapping the fetch, decode and execute stages of several instructions at once to increase throughput.'],
  ['Von Neumann architecture','A design where one memory holds both data and instructions, accessed over shared buses, executed in a sequential fetch–execute cycle.'],
  ['Compiler vs interpreter','A compiler translates the whole program into object code once; an interpreter translates and executes one statement at a time, every run.'],
  ['Validation vs verification','Validation checks data is reasonable/allowed (range, type, format, presence). Verification checks data was not changed during entry or transfer (double entry, visual check).'],
  ['Parity check','An extra bit set so the number of 1s is even (or odd); a mismatch on receipt reveals a single-bit transmission error.'],
  ['Checksum','A value computed from a block of data and sent with it; the receiver recomputes and compares to detect errors.'],
  ['Primary key','The field (or combination of fields) that uniquely identifies each record in a table.'],
  ['Foreign key','A field in one table that references the primary key of another table, creating the relationship between them.'],
  ['Normalisation to 3NF','Restructuring tables to remove redundancy: atomic values (1NF), no partial dependence on a composite key (2NF), no transitive/non-key dependence (3NF).'],
  ['DDL vs DML','DDL defines the database structure (CREATE, ALTER). DML works on the data itself (SELECT, INSERT, UPDATE, DELETE).'],
  ['Stack vs queue','Stack: last-in first-out, push/pop at one end. Queue: first-in first-out, add at the rear, remove at the front.'],
  ['Linked list','A dynamic structure of nodes, each holding data plus a pointer to the next node; a null pointer marks the end.'],
  ['Abstract data type (ADT)','A collection of data together with the operations allowed on it, defined independently of how it is implemented.'],
  ['Recursion','A routine defined in terms of itself, with a base case that stops the chain of calls; each call is stacked until the base case unwinds.'],
  ['Binary search','On a SORTED list, repeatedly compare the middle item and discard half the list — O(log n).'],
  ['Hash table','Keys are mapped to indexes by a hash function; collisions are handled by chaining or by probing to the next free slot.'],
  ['Symmetric vs asymmetric encryption','Symmetric: one shared secret key both encrypts and decrypts. Asymmetric: the public key encrypts, the matching private key decrypts.'],
  ['Digital certificate','An electronic document issued by a Certificate Authority binding a public key to its owner’s verified identity.'],
  ['Digital signature','A message digest encrypted with the sender’s private key — proves who sent the message and that it was not altered.'],
  ['TCP/IP stack','Application, Transport, Internet and Link layers; each layer adds (or strips) its own header as data passes down (or up).'],
  ['Packet vs circuit switching','Packet: the message is split into packets routed independently and reassembled. Circuit: a dedicated path is reserved for the whole transmission.'],
  ['Encapsulation (OOP)','Bundling data and the methods that act on it inside a class, keeping attributes private and exposing controlled get/set methods.'],
  ['Polymorphism (OOP)','The same method name behaving differently for different classes — typically by overriding an inherited method.'],
  ['BNF','Backus–Naur Form — a notation of production rules used to define the grammar (syntax) of a language precisely.'],
  ['Reverse Polish Notation','Postfix notation — operators follow their operands, needs no brackets, and is evaluated with a stack.'],
  ['Flip-flop','A logic circuit (SR or JK) that stores a single bit — the basis of registers and static memory.'],
  ['A* algorithm','Shortest-path search that expands the node with the lowest (cost so far + heuristic estimate to the goal).'],
];
